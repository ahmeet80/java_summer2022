package day04_dataCasting.data_casting_odev;

public class S7 {
    public static void main(String[] args) {
        byte sayi1= 10;
        short sayi2= 20;
        int sayi3= 30;
        float sayi4= 40.40f;
        double sayi5= 50.50;

        System.out.println("sayi1/sayi2: "+sayi1/sayi2);
        System.out.println("sayi2/sayi3: "+sayi2/sayi3);
        System.out.println("sayi3/sayi4: "+sayi3/sayi4);
        System.out.println("sayi4/sayi5: "+sayi4/sayi5);
        System.out.println("sayi5/sayi3: "+sayi5/sayi3);
    }
}
